def crear_coleccion_prestamo(prestamos):
    db = get_database()
    db.create_collection(
        "prestamo",
        validator = {
            "$jsonSchema":{
                "required":["fecha_prestamo", "fecha_devolucion", "numero_copia", "isbn_copia", "usuario_rut"],
                "properties":{
                    "fecha_prestamo":{
                        "bsonType":"string"
                    },
                    "fecha_devolucion":{
                        "bsonType": "string"
                    },
                    "numero_copia":{
                        "bsonType": "int"
                    },
                    "isbn_copia":{
                        "bsonType": "string"
                    },
                    "usuario_rut":{
                        "bsonType": "string"
                    }
                }
            }
        }
    )
    db.prestamo.create_index([("isbn_copia", 1),("numero_copia", 1),("usuario_rut", 1), ("fecha_prestamo", 1)], unique=True)
    db.prestamo.insert_many(prestamos)

